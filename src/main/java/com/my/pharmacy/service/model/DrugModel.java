package org.example.pharmacy.service.model;

import org.my.pharmacy.service.valueObjects.Price;

public class DrugModel {

    private Long id;
    private String name;
    private Price price;
    private String description; //to zle

    public DrugModel(Long id, String name, Price price, String description) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.description = description;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Price getPrice() {
        return price;
    }

    public void setPrice(Price price) {
        this.price = price;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}